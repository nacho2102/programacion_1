# Operadores de pertenencia
a = "A"
b = f"{a}"
# in verifica que exista el elemento dentro de la variable
print("A" in b)# -> True 
# not in verifica que no exista el elemento dentro de la variable
print("A" not in b)# -> False
#is verifica que el elemento sea el mismo
print("A" is b)# -> True
#is not verifica que el elemento no sea el mismo
print("A" is not b)# -> False